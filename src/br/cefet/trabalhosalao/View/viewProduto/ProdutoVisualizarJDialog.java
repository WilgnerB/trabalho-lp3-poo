    public ProdutoVisualizarJDialog(java.awt.Frame parent, boolean modal, int cdProduto) {
        super(parent, modal);
        initComponents();
        salao = Salao.getInstance();

        try {
            Produto p;
            p = salao.getProdutoPorCd(cdProduto);
            txtID.setText(p.getCdProduto() + "");
            txtDt.setText(p.getDtEntrada() + "");
            txtNome.setText(p.getNmProduto());
            txtPreco.setText(p.getVlAtual()+ "");
            txtQtd.setText(p.getQtdProduto()+ "");

            this.produto = p;
        } catch (DaoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }

